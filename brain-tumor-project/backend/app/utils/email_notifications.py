"""
Email Notification System — with dynamic APP_URL support
"""

import smtplib
from email.mime.text import MIMEText
from email.mime.multipart import MIMEMultipart
from datetime import datetime


def _get_config():
    try:
        from app.config import settings
        return {
            "smtp_host":       settings.EMAIL_SMTP,
            "smtp_port":       settings.EMAIL_PORT,
            "from_email":      settings.EMAIL_FROM,
            "from_pass":       settings.EMAIL_PASSWORD,
            "notify_critical": settings.NOTIFY_ON_CRITICAL,
            "app_url":         settings.APP_URL,
        }
    except Exception as e:
        print(f"[Email] Config load error: {e}")
        return {
            "smtp_host":       "smtp.gmail.com",
            "smtp_port":       587,
            "from_email":      "",
            "from_pass":       "",
            "notify_critical": True,
            "app_url":         "http://localhost:5173",
        }


def _app_url() -> str:
    return _get_config()["app_url"]


def _send(to: str, subject: str, html: str) -> bool:
    cfg        = _get_config()
    from_email = cfg["from_email"]
    from_pass  = cfg["from_pass"]

    if not from_email or not from_pass:
        print(f"[Email] Skipped (no credentials in .env)")
        print(f"[Email]   EMAIL_FROM={repr(from_email)}")
        print(f"[Email]   EMAIL_PASSWORD={'set' if from_pass else 'EMPTY'}")
        return False

    try:
        msg = MIMEMultipart("alternative")
        msg["Subject"] = subject
        msg["From"]    = f"NeuralOnco <{from_email}>"
        msg["To"]      = to
        msg.attach(MIMEText(html, "html"))

        with smtplib.SMTP(cfg["smtp_host"], cfg["smtp_port"]) as s:
            s.ehlo()
            s.starttls()
            s.login(from_email, from_pass)
            s.sendmail(from_email, to, msg.as_string())

        print(f"[Email] ✓ Sent to {to}: {subject}")
        return True

    except smtplib.SMTPAuthenticationError:
        print("[Email] ✗ Authentication failed — wrong App Password or Gmail blocked")
        return False
    except smtplib.SMTPConnectError:
        print(f"[Email] ✗ Cannot connect to SMTP server")
        return False
    except Exception as e:
        print(f"[Email] ✗ Failed: {e}")
        return False


