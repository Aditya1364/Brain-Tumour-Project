def _row(label: str, value: str) -> str:
    return f'<div style="display:flex;justify-content:space-between;padding:8px 0;border-bottom:1px solid rgba(0,212,255,0.1);"><span style="color:#6B7DB3;font-size:13px;">{label}</span><span style="color:#E8EEFF;font-size:13px;font-weight:600;">{value}</span></div>'


def _btn(text: str, url: str, color: str = "linear-gradient(135deg,#7B2FFF,#00D4FF)") -> str:
    return f'<div style="margin-top:20px;text-align:center;"><a href="{url}" style="display:inline-block;padding:12px 28px;background:{color};color:#fff;text-decoration:none;border-radius:30px;font-size:13px;font-weight:600;">{text}</a></div>'


